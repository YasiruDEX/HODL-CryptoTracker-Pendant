void loop() {

  if(!wifiManager.isConnected()){

    if(WiFi_connected){

      // Connect to Wi-Fi
      WiFi.mode(WIFI_AP);
      WiFi.softAP(ssid, password);
      
      // Print the IP address
      Serial.println(WiFi.softAPIP());

      // Define webpage routes
      server.on("/", HTTP_GET, handleRoot);
      server.on("/update", HTTP_GET, handleUpdate);
      
      // Start server
      server.begin();
    }
    WiFi_connected = false;

    server.handleClient();

    Serial.print(SSID);
    Serial.print(" : ");
    Serial.println(PASSWORD);

    if (SSID != prevSSID or PASSWORD != prevPASSWORD){
      wifiManager.ssid = SSID;
      wifiManager.password = PASSWORD;
      initialize();
      WiFi_connected = true;
    }

    prevSSID = SSID;
    prevPASSWORD = PASSWORD;

  }else{
  
        httpUrl = "https://www.okx.com/api/v5/market/ticker?instId=" + coin + "-USDT-SWAP";

        unsigned long currentMillis = millis();

        int priceInt = Price.toInt();
        int previousPriceInt = previousPrice.toInt(); // Convert previousPrice to integer

        if (currentMillis - previousMillis >= interval) {
          previousMillis = currentMillis;
          previousPrice = Price;
          coin = firebase.getString("current_coin");
          Serial.println(coin);
          httpUrl = "https://www.okx.com/api/v5/market/ticker?instId=" + coin + "-USDT-SWAP";
          makeHttpRequest();
          if (Price.toInt() - previousPrice.toInt() < 30000) {
              // Shift values in the chart_values array
              for (int i = 0; i < CHART_VALUES_SIZE - 1; ++i) {
                  chart_values[i] = chart_values[i + 1];
              }

              // Set the last element to the difference of Price and previousPrice
              chart_values[CHART_VALUES_SIZE - 1] = Price.toInt() - previousPrice.toInt();
          }

          // Print the updated chart_values array
          Serial.print("Chart Values: ");
          for (int i = 0; i < CHART_VALUES_SIZE; ++i) {
              Serial.print(chart_values[i]);
              Serial.print(", ");
          }
          Serial.println();
        }



        // picture loop
        u8g2.clearBuffer();
        u8g2_prepare();
        u8g2_string(draw_state & 7);
        u8g2.sendBuffer();

        // increase the state
        draw_state++;

        if (draw_state >= 12 * 8){
          draw_state = 0;
          }

        // deley between each page
        delay(100);

        // Serial.print("Old Price: ");
        // Serial.print(previousPrice);
        // Serial.print(", New Price: ");
        // Serial.println(Price);

        if (priceInt > previousPriceInt) {
          up = true;
        } else if (priceInt < previousPriceInt) {
          up = false;
        }

        // Other non-blocking tasks can be performed here
  }
}

